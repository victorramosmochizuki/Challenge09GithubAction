package modelo;

public class Cliente {

    private int idcli;
    private String nomclie;
    private String apeclie;
    private String docclie;
    private String emaclie;
    private String celclie;
    private String estclie;

    public int getIdcli() {
        return idcli;
    }

    public void setIdcli(int idcli) {
        this.idcli = idcli;
    }

    public String getNomclie() {
        return nomclie;
    }

    public void setNomclie(String nomclie) {
        this.nomclie = nomclie;
    }

    public String getApeclie() {
        return apeclie;
    }

    public void setApeclie(String apeclie) {
        this.apeclie = apeclie;
    }

    public String getDocclie() {
        return docclie;
    }

    public void setDocclie(String docclie) {
        this.docclie = docclie;
    }

    public String getEmaclie() {
        return emaclie;
    }

    public void setEmaclie(String emaclie) {
        this.emaclie = emaclie;
    }

    public String getCelclie() {
        return celclie;
    }

    public void setCelclie(String celclie) {
        this.celclie = celclie;
    }

    public String getEstclie() {
        return estclie;
    }

    public void setEstclie(String estclie) {
        this.estclie = estclie;
    }

}

